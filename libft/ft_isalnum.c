#include "stdlib.h"

int	ft_isalnum(int sym)
{
	if ((sym >= 48 && sym <= 57) || (sym >= 65 && sym <= 90)
	|| (sym >= 97 && sym <= 122))
		return (1);
	else
		return (0);
}
