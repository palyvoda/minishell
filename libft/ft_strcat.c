char	*ft_strcat(char *str1, const char *str2)
{
	char	*cur1;
	char	*cur2;

	cur1 = str1;
	cur2 = (char *)str2;
	while (*cur1 != '\0')
		cur1++;
	while (*cur2 != '\0')
	{
		*cur1 = *cur2;
		cur1++;
		cur2++;
	}
	*cur1 = '\0';
	return (str1);
}
