#include "libft.h"

void	ft_lstiter(t_list1 *lst, void (*f)(t_list1 *elem))
{
	t_list1 *tmp;

	while (lst)
	{
		tmp = lst->next;
		(*f)(lst);
		lst = tmp;
	}
}
